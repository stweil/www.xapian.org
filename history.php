<h1>History</h1>

<p>Xapian is a modern class library, but has evolved out of over 25 years of
commercial and academic experience.</p>

<p>The Xapian project is based upon the code of Open Muscat (also known as
Omsee) originally developed and released under the GPL by BrightStation PLC.
BrightStation is no longer trading, but some of the original developers from
BrightStation and others from the Open Muscat community decided to continue
development of the code.  Before the name Xapian was chosen, the project was
briefly known as Omseek.</p>

<p>BrightStation's Open Muscat project was to be a replacement for the
Muscat 3.6 information retrieval system,
which was originally written by Dr. Martin Porter at Cambridge
University.  In 1984, Martin and John Snyder founded Cambridge CD Publishing
to commercially exploit the technology; the company was soon renamed
Muscat Ltd when the focus shifted from CDs to the web.  Muscat Ltd was
bought by Maid PLC, who renamed themselves first to Dialog Corporation, and
then to BrightStation PLC (when they sold the Dialog brand and content
to Thomson).</p>

<p>Open Muscat (using the muscat36 backend) was the retrieval engine which powered
BrightStation's Webtop search engine
(<a href="http://web.archive.org/web/20000708023501/http://www.webtop.com/index.html">archive.org snapshot</a>),
which offered a search over around
500 million web pages (around 1.5 terabytes of database files).  Searches
took less than a second.  The whole search engine, including the web crawlers
and index building, ran on 30 Intel boxes.  Webtop also incorporated
technology from Muscat Ltd's EuroFerret search engine.
</p>

